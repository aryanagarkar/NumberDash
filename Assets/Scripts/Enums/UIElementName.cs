/// <summary>
/// Represents the UI elements used in the game.
/// </summary>

namespace HelperEnums
{
   public enum UIElementName
   {
      LevelPanel,          // Represents the screen where players choose the game level.
      TypeOfGamePanel,     // Represents the screen where players choose the variation of the game.
      Settings,            // Represents the screen where players can adjust game settings.
      InstructionsPage,    // Represents the screen displaying game instructions.
      CharacterPanel,      // Represents the screen where players can choose their opponent's (the computer) character.
      GameOver,            // Represents the screen displayed when the game is over.
      ScoreMessage         // Represents the screen displaying whether the game ended in a loss, win, or tie. 
   }
}